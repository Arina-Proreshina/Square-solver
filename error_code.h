#ifndef ERROR_CODE_H
#define ERROR_CODE_H

/**
 * @enum ErrorCode
 * @brief Коды ошибок для программы.
 *
 * Это перечисление содержит коды, которые используются для обозначения
 * успешного выполнения программы или ошибки.
 */
enum {
    SUCCESS = 0,    /**< Успешное выполнение операции. */
    ERROR_CODE = -1 /**< Ошибка выполнения операции. */
} ErrorCode;

#endif // ERROR_CODE_H
